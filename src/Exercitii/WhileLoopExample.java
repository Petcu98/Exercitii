public class WhileLoopExample {
    public static void main(String[] args) {


    /*
    Implement the program displaying all the numbers from the range 5 - 100 (use ‘while’
loop).
     */
        int i = 5;
        while (i < 101) {
            System.out.println(i++);
        }
    }
}
